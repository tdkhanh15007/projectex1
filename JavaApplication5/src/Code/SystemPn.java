    public void loadGroup() {
        DefaultTableModel model = (DefaultTableModel) tbGroups.getModel();
        model.setRowCount(0);
        Vector<GroupsBean> item = gb.displayAll();
        Vector v;
        for (int i = 0; i < item.size(); i++) {
            v = new Vector();
            GroupsBean temp = item.get(i);
            int id = temp.group_id;
            String types = temp.group_type;
            float gcharges = temp.charges;
            v.add(id);
            v.add(types);
            v.add(gcharges);
            model.addRow(v);
        }
    }

    public void loadAct() {
        DefaultTableModel model = (DefaultTableModel) tbAct.getModel();
        model.setRowCount(0);
        Vector<ActBean> item = acb.displayAll();
        Vector v;
        for (int i = 0; i < item.size(); i++) {
            v = new Vector();
            ActBean temp = item.get(i);
            int id = temp.act_id;
            String act = temp.act_type;
            float acharges = temp.charges;
            v.add(id);
            v.add(act);
            v.add(acharges);
            model.addRow(v);
        }
    }

